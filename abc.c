#include <curses.h>
#include <signal.h>
#include <unistd.h>
#include "abc.h"

int add_abc(int x);
void option(char *str);
int my_mvaddstr(int y, int x, char *str);

int my_mvaddstr(int y, int x, char *str)
{
    for ( ; x < 0; ++x, ++str)
        if (*str == '\0')  return ERR;
    for ( ; *str != '\0'; ++str, ++x)
        if (mvaddch(y, x, *str) == ERR)  return ERR;
    return OK;
}

int main(int argc, char *argv[])
{
    int x;

    initscr();
    signal(SIGINT, SIG_IGN);
    noecho();
    curs_set(0);
    nodelay(stdscr, TRUE);
    leaveok(stdscr, TRUE);
    scrollok(stdscr, FALSE);

    for (x = COLS - 1; ; --x) {
        if (add_abc(x) == ERR) break;
        getch();
        refresh();
        usleep(40000);
    }
    mvcur(0, COLS - 1, LINES - 1, 0);
    endwin();

    return 0;
}


int add_abc(int x)
{
    static char *abc[ABCHEIGHT + 1]
        = {ABC1, ABC2, ABC3, ABC4, ABC5, ABC6, ABC7, ABC8, ABC9, ABC10, ABC11, ABC12, ABC13, ABC14, ABCDEL};

    int i, y, py1 = 0;

    if (x < - ABCLENGTH)  return ERR;
    y = LINES / 2 - 3;

    for (i = 0; i <= ABCHEIGHT; ++i) {
        my_mvaddstr(y + i + py1, x + 1, abc[i]);
    }
    return OK;
}
